import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const sidebarVariants = cva(
  "flex h-full w-full flex-col overflow-hidden bg-sidebar text-sidebar-foreground",
  {
    variants: {
      variant: {
        default: "border-r",
        floating: "rounded-lg border shadow-lg",
        inset: "rounded-lg border shadow-sm bg-sidebar/50 backdrop-blur-sm",
      },
      side: {
        left: "",
        right: "",
      },
      collapsible: {
        none: "",
        icon: "",
        offcanvas: "",
      },
    },
    defaultVariants: {
      variant: "default",
      side: "left",
      collapsible: "none",
    },
  }
)

const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & VariantProps<typeof sidebarVariants>
>(({ className, variant, side, collapsible, ...props }, ref) => {
  return (
    <div
      ref={ref}
      data-side={side}
      data-variant={variant}
      data-collapsible={collapsible}
      className={cn(sidebarVariants({ variant, side, collapsible }), className)}
      {...props}
    />
  )
})
Sidebar.displayName = "Sidebar"

